void printMap(char mapa[27][77]) {
    for (int i = 0; i < 27; i++) {
        for (int j = 0; j < 77; j++) {
            if (mapa[i][j] == 'o') {
                printf("o");
            }
            else if (mapa[i][j] == '@') {
                printf("C");
            }
            else if (mapa[i][j] == 'R') {
                printf("R");
            }
            else if (mapa[i][j] == 'B') {
                printf("B");
            }
            else if (mapa[i][j] == 'P') {
                printf("P");
            }
            else if (mapa[i][j] == 'O') {
                printf("O");
            }
            else if (mapa[i][j] == '=') {
                printf("=");
            }
            else if (mapa[i][j] == ' ') {
                printf(" ");
            }
            else if (mapa[i][j] == '1') {
                printf(".");
            }
            else if (mapa[i][j] == '#') {
                printf("@");
            }
            else if (mapa[i][j] == '0') {
                printf("─");
            }
            else if (mapa[i][j] == '2') {
                printf("│");
            }
            else if (mapa[i][j] == '3') {
                printf("┐");
            }
            else if (mapa[i][j] == '4') {
                printf("┘");
            }
            else if (mapa[i][j] == '5') {
                printf("└");
            }
            else if (mapa[i][j] == '6') {
                printf("┌");
            }
        }
        printf("\n");  
    }
}

